'use strict';

const seq = (f) => (g) => (typeof g === 'number' ? f(g) : seq((x) => f(g(x))));
/*
Я не просто скопіював, просто я ледь зрозумів її за 10 хвилин розбору, через шедевральне виконання
Це функція яка бере пареметром якусь функцію f і повертає якусь функцію, яка бере параметром g.
Остання функція перевіряє, якщо g - число : видає f(g), інакше видає seq в яку я аргумент дає таку функцію,
Щоб це все просто зациклилось, поки g не стане числом
*/
module.exports = { seq };
